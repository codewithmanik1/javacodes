package Basics;


public class OperatorsOne {

    public static void main(String[] args) {
        //Assignment operator
        // =

        int a = 20;
        System.out.println(a);              //20-p, 20-n, 20-d

        int b = a + 80;
        a = b;
        System.out.println(a + b);          //200-p, 200-m, 200-d
    }
}
