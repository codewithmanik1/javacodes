package Basics;

public class ProgramOne {
    public static void main(String[] args) {

        int a = 66;
        int b = a + 'A';

        System.out.println(a);          //66
        System.out.println(a + b);      //66 + 66 + 65

        a = 60;
        int c = 69;
        System.out.println(c);    //69
    }
}
